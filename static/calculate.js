function calculate(age){
	var form = document.querySelector('form');
	// idea from https://codepen.io/loilo/pen/YYjbgE?editors=1010
	// check if input has been valid
	if (!form.checkValidity()) {
		// Create the temporary button, click and remove it
		var tmpSubmit = document.createElement('button');
		form.appendChild(tmpSubmit);
		tmpSubmit.click();
		form.removeChild(tmpSubmit);
		document.getElementById("result").innerHTML = "";
	}
	// input is valid
	else {
		// find the age group
		if (age < 22){
			var group = 1;
		} else if (age == 22){
			var group = 2;
		} else{
			var group = Math.floor((age-22)/3)+2;
		}
		// get pushup, parse as int to settle problems with leading 0
		var pushup = parseInt(document.getElementById("pushup").value);
		if (pushup>60) {pushup=60;}
		// get situp, parse as int to settle problems with leading 0
		var situp = parseInt(document.getElementById("situp").value);
		if (situp>60) {situp=60;}

		// get minute
		var min = document.getElementById("min").value;
		// remove leading 0
		min = min.replace(/\b0+/g, '');

		// get seconds
		var sec = document.getElementById("sec").value;
		// if second is 0 format it accordingly
		if (parseInt(sec)==0){
			sec = "00";
		}
		// else remove leading 0
		else{sec=sec.replace(/\b0+/g, '');}

		// format time
		var time = "".concat(min,":",sec);

		//tabulate the score
		var score = p[pushup][group] + s[situp][group] + r[time][group];
		var grade = ""
		
		// find out the grade
		if (score>= 90){grade="COMMANDO GOLD";}
		else if (score>=85){grade="GOLD";}
		else if (score>=75){grade="SILVER";}
		else if (score>=61){grade="PASS WITH INCENTIVE";}
		else if (score>=51){grade="PASS";}
		else {grade="FAIL";}

		// Output the result
		text = "".concat(grade,": ",score);
		document.getElementById("result").innerHTML = text;
		document.getElementById("score").value= text;
		
	}
}